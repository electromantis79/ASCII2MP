void ClearEtn(unsigned char mask)
{
	unsigned char i;
	unsigned char j;
	unsigned char k;
	for(k = 0; k < EtnPanels(); k++)
	{
		for(j = 0; j < EtnPanelHeight(); j++)
		{
			for(i = 0; i < ETN_PANEL_WIDTH; i++)
			{
				cEtnLeds[i][j][k] = mask;
			}
		}
	}
}

void SetEtnText(unsigned char mask, char *cText)
{
	unsigned char maskCharBitsData;
	const unsigned char* pbyCharBits;
	const unsigned char* pbyCharBits2;
	const TFontChar8* pbits;
	const TFont* Alpha;
	unsigned char bySpacing;
	char cCurrent;			// Get the next character
	signed int i, j, k;
	signed int nLedColumns;
	volatile unsigned char w;
	unsigned char c = *cText;	// Get formating
	cText++;					// Inc to first text location
	switch(g_byPanelHeight)
	{
	case 7:
	case 14:
		switch(c/6)
		{
		case 2:
			Alpha = &Alpha14wt;
			break;
		case 0:
		default:
			Alpha = &Alpha14ct;
			break;
		case 1:
			Alpha = &Alpha14t;
			break;
		}
		if((mask & 0x11) && (g_byPanelHeight == 7))
		{
			//Alpha += sizeof(Alpha14t);		// Does not work
			//Alpha += &Alpha14b - &Alpha14t;	// This does but is long
			Alpha += 1;							// This "increments to the next element" of Alpha "Array"
		}
		bySpacing = 2;
		break;
	case 9:
	default:
		switch(c/6)
		{
		case 2:
			Alpha = &Alpha9w;
			bySpacing = 1;
			break;
		case 0:
		default:
			Alpha = &Alpha9p;
			bySpacing = 1;
			break;
		case 1:
			Alpha = &Alpha9;
			bySpacing = 1;
			break;
		}
		break;
	}

	nLedColumns = 0;
	for(j = 0; j < strlen(cText); j++)
	{
		cCurrent = cText[j];			// Get the next character
		pbits = Alpha->ft[cCurrent-' '];
		//nLedColumns += pbits->byWidth/g_byPanelHeight + 1;
		nLedColumns += pbits->byWidth/EtnSplitHeight() + bySpacing;
	}
		nLedColumns--;

	j = k = 0;
	// Calculate the column the text starts at
	switch(c%3)
	{
	case 2:
		i = ETN_PANEL_WIDTH*ETN_NUM_PANELS - nLedColumns + bySpacing - 1;		//Right
		break;
	case 0:
		i = 0;													//Left
		break;
	case 1:
	default:
		i = (ETN_PANEL_WIDTH*ETN_NUM_PANELS + bySpacing - 1 - nLedColumns)/2;	//Center
		break;
	}
	// Now make that an offset of a specific panel
	while(i < 0)
	{
		// Negative column means the text starts off the left-most panel
		i += ETN_PANEL_WIDTH;
		k--;
	}
	while(i >= ETN_PANEL_WIDTH)
	{
		i -= ETN_PANEL_WIDTH;
		k++;
	}
	do
	{
		cCurrent = *cText;			// Get the next character
		if(cCurrent == 0)
			break;					// Done now
		if(EtnFontHeight()>EtnSplitHeight())
		{
			pbits = Alpha->ft[cCurrent-' '+NUM_CHARACTERS];	// Get the bottom half font for that character
			pbyCharBits2 = &pbits->byData[0];
		}
		else
			pbyCharBits2 = NULL;
		pbits = Alpha->ft[cCurrent-' '];	// Get the font for that character
		pbyCharBits = &pbits->byData[0];
		maskCharBitsData = 0x80;
		// Place the bits for this character in the proper place
		for(w = pbits->byWidth; w != 0; w--)
		{
			// Place each bit from the font for this character
			if((maskCharBitsData & *pbyCharBits) && (k >= 0))
				cEtnLeds[i][j][k] ^= mask;
			if(pbyCharBits2)
				// We have a font taller than the panel, process the xxxb font
				if((maskCharBitsData & *pbyCharBits2) && (k >= 0))
					(cEtnLeds[i][j+EtnSplitHeight()][k]) ^= mask;
			j++;	// Inc to next LED
			if(j == EtnSplitHeight())
			{
				j = 0;	// Done with column
				i++;	// Inc to next column
				if(i >= ETN_PANEL_WIDTH)
				{
					i = 0;	// Done with panel
					k++;	// Inc to next panel
					if(k >= ETN_NUM_PANELS)
					{
						//k = 0;	// 
						break;		// Done with last panel, quit
					}
				}
			}
			maskCharBitsData >>= 1;
			if(maskCharBitsData == 0)
			{
				// Get the next column of bits
				pbyCharBits++;
				if(pbyCharBits2)
					pbyCharBits2++;
				maskCharBitsData = 0x80;
			}
		}
		// Done with the character
		if(k >= ETN_NUM_PANELS)
			// If we are off the last panel, quit
			break;
		cText++;
		j = 0;
		{
			unsigned char n;
			for(n = 0; n < bySpacing; n++)
				i++;
		}
		if(i >= ETN_PANEL_WIDTH)
		{
			i -= ETN_PANEL_WIDTH;
			k++;
			if(k >= ETN_NUM_PANELS)
			{
				k = 0;
				break;
			}
		}
	} while (1);
}

